package main

import (
	"fmt"
	"os"
	"os/user"
	"time"

	"github.com/shirou/gopsutil/cpu"
	"github.com/shirou/gopsutil/host"
	"github.com/shirou/gopsutil/mem"
)

func main() {
	usr, _ := user.Current()
	hostname, _ := os.Hostname()
	info, _ := host.Info()

	cpus, _ := cpu.Info()
	cpuName := "Unknown"
	if len(cpus) > 0 {
		cpuName = cpus[0].ModelName
	}

	vmStat, _ := mem.VirtualMemory()

	fmt.Println("⡏⠉⠛⢿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⡿⣿")
	fmt.Println("⣿⠀⠀⠀⠈⠛⢿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⣿⠿⠛⠉⠁⠀⣿")
	fmt.Printf("⣿⣧⡀⠀⠀⠀⠀⠙⠿⠿⠿⠻⠿⠿⠟⠿⠛⠉⠀⠀⠀⠀⠀⣸⣿   User:      %s\n", usr.Username)
	fmt.Printf("⣿⣿⣷⣄⠀⡀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⢀⣴⣿⣿   Host:      %s\n", hostname)
	fmt.Printf("⣿⣿⣿⣿⠏⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠠⣴⣿⣿⣿⣿   OS:        %s %s (%s)\n", info.Platform, info.PlatformVersion, info.KernelArch)
	fmt.Printf("⣿⣿⣿⡟⠀⠀⢰⣹⡆⠀⠀⠀⠀⠀⠀⣭⣷⠀⠀⠀⠸⣿⣿⣿⣿   Uptime:    %s\n", time.Duration(info.Uptime)*time.Second)
	fmt.Printf("⣿⣿⣿⠃⠀⠀⠈⠉⠀⠀⠤⠄⠀⠀⠀⠉⠁⠀⠀⠀⠀⢿⣿⣿⣿   CPU:       %s\n", cpuName)
	fmt.Printf("⣿⣿⣿⢾⣿⣷⠀⠀⠀⠀⡠⠤⢄⠀⠀⠀⠠⣿⣿⣷⠀⢸⣿⣿⣿   Memory:    %.2f GB / %.2f GB\n", float64(vmStat.Used)/1024/1024/1024, float64(vmStat.Total)/1024/1024/1024)
	fmt.Println("⣿⣿⣿⡀⠉⠀⠀⠀⠀⠀⢄⠀⢀⠀⠀⠀⠀⠉⠉⠁⠀⠀⣿⣿⣿")
	fmt.Println("⣿⣿⣿⣧⠀⠀⠀⠀⠀⠀⠀⠈⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⢹⣿⣿")
	fmt.Println("⣿⣿⣿⣿⠃⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⠀⢸⣿⣿")
}
